   function addToCart() {
    const quantity = parseInt(document.getElementById("quantity").value);
    const selectedSizeButton = document.querySelector(".size-options button.selected");
    const size = selectedSizeButton ? selectedSizeButton.textContent : null;

    if (!size) {
        alert("Please select a size before adding to the cart.");
        return;
    }

    const productDetails = {
        product_id: 1, // Example product ID
        name: "StyleGen Men Striped Print Drop Shoulder Tee",
        size: size,
        quantity: quantity,
        price: 407.00, // Example price
        color: document.getElementById("selectedColor").textContent,
        image: "G1.png" // Example product image
    };

    // Send data to the server
    $.ajax({
        url: 'addtocart.php',
        type: 'POST',
        data: productDetails,
        success: function (response) {
            const result = JSON.parse(response);
            if (result.success) {
                alert("Item added to cart successfully!");
                updateCartCount();
            } else {
                alert("Failed to add item to the cart.");
            }
        },
        error: function () {
            alert("An error occurred while adding to the cart.");
        }
    });
}

function updateCartCount() {
    $.ajax({
        url: 'getcart.php', // Endpoint to fetch cart count
        success: function (response) {
            const count = parseInt(response);
            document.getElementById("cartCount").textContent = count;
        }
    });
}
