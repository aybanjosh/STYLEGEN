    <script>
    function loadProductRating(productId) {
    fetch(`get_product_rating.php?product_id=${productId}`)
        .then(response => response.json())
        .then(data => {
            if (data.success) {
                // Update average rating
                document.getElementById('averageRating').textContent = data.average_rating.toFixed(1);

                // Dynamically update stars
                const ratingStars = document.getElementById('ratingStars');
                const fullStars = Math.floor(data.average_rating);
                const halfStar = data.average_rating % 1 >= 0.5 ? 1 : 0;
                const emptyStars = 5 - fullStars - halfStar;

                let starsHtml = '★'.repeat(fullStars); // Full stars
                if (halfStar) starsHtml += '½'; // Half star
                starsHtml += '☆'.repeat(emptyStars); // Empty stars
                ratingStars.innerHTML = starsHtml;

                // Update review count
                document.getElementById('reviewCount').textContent = data.review_count;
            } else {
                console.error('Failed to load product rating:', data.message);
            }
        })
        .catch(error => console.error('Error fetching product rating:', error));
}

// Call the function when the page is loaded
document.addEventListener('DOMContentLoaded', function () {
    const productId = 8; // Replace with your dynamic product ID
    loadProductRating(productId);
});

