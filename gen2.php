<script>
    let currentColor = "White"; // Track the current color

    // Function to change the main image and update the color
    function changeMainImage(imageSrc, color) {
        document.getElementById("mainProductImage").src = imageSrc;
        currentColor = color; // Update the current color
        document.getElementById("selectedColor").textContent = color; // Update displayed color
    }

    // Function to show the selected color when main image is clicked
    function showSelectedColor() {
        alert(`Current Color: ${currentColor}`);
    }

    // Function to change the color and update sidebar images
    function changeColor(color) {
        const sidebarImages = document.querySelector(".sidebar-images");
        if (color === "red") {
            sidebarImages.innerHTML = `
                <img src="red.png" alt="Black Thumbnail 1" onclick="changeMainImage('red.png', 'red')">
                <img src="red1.png" alt="Black Thumbnail 2" onclick="changeMainImage('red1.png', 'red')">

            `;
            changeMainImage("red.png", "red"); // Update to Black
        } else if (color === "black") {
            sidebarImages.innerHTML = `
                <img src="blackdress.png" alt="Thumbnail Multicolor 1" onclick="changeMainImage('blackdress.png', 'Multicolor')">
                <img src="blackdress1.png" alt="Thumbnail Multicolor 2" onclick="changeMainImage('blackdress1.png', 'Multicolor')">
               
            `;
            changeMainImage("blackdress.png", "black"); // Update to Multicolor
        }
    }

    // Function to highlight the selected size
    function selectSize(button) {
        document.querySelectorAll(".size-options button").forEach((btn) => btn.classList.remove("selected"));
        button.classList.add("selected");
    }
</script>


  
