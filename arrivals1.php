<script>
    document.getElementById('reviewForm').addEventListener('submit', function (e) {
    e.preventDefault();

    const reviewText = document.getElementById('reviewText').value.trim();
    const reviewRating = document.getElementById('reviewRating').value;
    const reviewImages = document.getElementById('reviewImages').files;
    const productId = document.getElementById('productId').value; // Product ID set to 14

    // Debugging: Log the fields to ensure correct values
    console.log('Review Text:', reviewText);
    console.log('Review Rating:', reviewRating);
    console.log('Product ID:', productId);

    if (!reviewText || !reviewRating || !productId) {
        alert('Please fill out all required fields.');
        return;
    }

    const formData = new FormData();
    formData.append('reviewText', reviewText);
    formData.append('reviewRating', reviewRating);
    formData.append('product_id', productId); // Send product_id

    for (let i = 0; i < reviewImages.length; i++) {
        formData.append('reviewImages[]', reviewImages[i]);
    }

    fetch('submit_review.php', {
        method: 'POST',
        body: formData,
    })
        .then(response => response.json())
        .then(data => {
            console.log('Server Response:', data);
            if (data.success) {
                alert('Review submitted successfully!');
                document.getElementById('reviewForm').reset();
                loadReviews(productId); // Reload reviews for this product
            } else {
                alert(`Error: ${data.message}`);
                console.error('Server Error:', data.message);
            }
        })
        .catch(error => {
            console.error('Error submitting review:', error);
            alert('Something went wrong. Please check the console for details.');
        });
});

function loadReviews(productId) {
    fetch(`fetch_reviews.php?product_id=${productId}`)
        .then(response => response.json())
        .then(data => {
            const dynamicReviews = document.getElementById('dynamicReviews');
            dynamicReviews.innerHTML = ''; // Clear existing reviews

            let totalReviews = 0;

            data.reviews.forEach(review => {
                const imagesHtml = review.images
                    .map(
                        image => `<img src="${image}" alt="Review Image" class="img-fluid" style="width: 150px; height: auto; border: 1px solid #ddd; border-radius: 4px; margin-right: 10px;">`
                    )
                    .join('');

                const newReview = `
                    <div class="review-card mb-4 border-bottom pb-3">
                        <div class="d-flex justify-content-between align-items-center">
                            <div>
                                <span class="review-date"><strong>User ID: ${review.user_id}</strong> ${review.review_date}</span>
                                <span class="review-rating ms-3" style="color: #FFD700;">${'★'.repeat(review.rating)}</span>
                            </div>
                        </div>
                        <div class="d-flex">
                            <div class="flex-grow-1">
                                <p>${review.review}</p>
                            </div>
                            <div class="ms-3 d-flex">
                                ${imagesHtml}
                            </div>
                        </div>
                    </div>`;
                dynamicReviews.insertAdjacentHTML('beforeend', newReview);
                totalReviews++;
            });

            // Update the reviews count
            document.getElementById('reviewsCount').textContent = `(${totalReviews})`;
        })
        .catch(error => console.error('Error loading reviews:', error));
}

// Call loadReviews on page load
document.addEventListener('DOMContentLoaded', function () {
    const productId = document.getElementById('productId').value;
    loadReviews(productId);
});
</script>



